/**
* Class for handlebar object
*
* @author Claire Foran 115379021
*/
public class Handlebar {
    private String name;

    /**
    * Constructor for the handlebar
    */
    public Handlebar(){}

    /**
    * Return the name of the handlebar
    */
    public String getName(){
        return this.name;
    }

    /**
    * Set the name of the handlebar
    */
    public void setName(String handlebarName){
        this.name = handlebarName;
    }

}